print("--- BƯỚC 2: KHỞI TẠO VÀ HUẤN LUYỆN MÔ HÌNH ---")

img_width, img_height = 200, 200
batch_size = 32

train_dir = out_dir / 'train'
test_dir = out_dir / 'test'

def crop_center(image):
    h, w, c = image.shape
    min_dim = min(h, w)
    start_x = (w // 2) - (min_dim // 2)
    start_y = (h // 2) - (min_dim // 2)
    cropped_image = image[start_y:start_y+min_dim, start_x:start_x+min_dim]
    return tf.image.resize(cropped_image, [img_width, img_height]).numpy()

# ---------------------------------------------------------
# TẠO GENERATORS (Đã giảm nhẹ độ nhiễu để không làm mất đường chỉ tay)
# ---------------------------------------------------------
train_datagen = ImageDataGenerator(
    rescale=1.0/255,
    rotation_range=15,       # Giảm độ xoay
    width_shift_range=0.1,   # Giảm độ xê dịch
    height_shift_range=0.1,
    zoom_range=0.1,          # Giảm độ zoom
    horizontal_flip=True,
    fill_mode="nearest",
    # Đã bỏ channel_shift và brightness để giữ nguyên cấu trúc vân tay
    preprocessing_function=crop_center
)

test_datagen = ImageDataGenerator(
    rescale=1.0/255,
    preprocessing_function=crop_center
)

train_generator = train_datagen.flow_from_directory(
    train_dir,
    target_size=(img_width, img_height),
    batch_size=batch_size,
    class_mode="categorical"
)

validation_generator = test_datagen.flow_from_directory(
    test_dir,
    target_size=(img_width, img_height),
    batch_size=batch_size,
    class_mode="categorical",
    shuffle=False
)

# ---------------------------------------------------------
# XÂY DỰNG KIẾN TRÚC MÔ HÌNH (Bổ sung Batch Normalization)
# ---------------------------------------------------------
model = Sequential([
    Conv2D(32, (3,3), activation="relu", input_shape=(img_width, img_height, 3)),
    BatchNormalization(), # Giúp mô hình ổn định và học nhanh hơn
    MaxPooling2D(2,2),

    Conv2D(64, (3,3), activation="relu"),
    BatchNormalization(),
    MaxPooling2D(2,2),

    Conv2D(128, (3,3), activation="relu"),
    BatchNormalization(),
    MaxPooling2D(2,2),

    Conv2D(256, (3,3), activation="relu", padding="same"),
    BatchNormalization(),
    MaxPooling2D(2,2),

    Flatten(),
    Dense(256, activation="relu"),
    BatchNormalization(),
    Dropout(0.5), # Chống học vẹt
    Dense(6, activation="softmax") # 6 Cung chỉ tay
])

# Chỉnh lại tốc độ học (learning rate) nhỏ lại một chút để AI học cẩn thận hơn
optimizer = Adam(learning_rate=0.0005)

model.compile(
    optimizer=optimizer,
    loss="categorical_crossentropy",
    metrics=["accuracy"]
)
model.summary()

# ---------------------------------------------------------

# ---------------------------------------------------------
# Bạn muốn bỏ Early Stopping, nên nó sẽ chạy đủ 100% số Epochs bạn đặt
epochs = 80
history = model.fit(
    train_generator,
    validation_data=validation_generator,
    epochs=epochs
    # Bỏ callbacks=[early_stopping] ở đây
)


def preprocess_palm(image_path):
    img = cv2.imread(image_path)
    if img is None: return None

    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(5, 5))
    enhanced_gray = clahe.apply(gray)
    blurred = cv2.medianBlur(enhanced_gray, 1)

    _, thresh = cv2.threshold(blurred, 80, 255, cv2.THRESH_BINARY)
    contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    roi = None
    if contours:
        largest_contour = max(contours, key=cv2.contourArea)
        M = cv2.moments(largest_contour)
        if M["m00"] != 0:
            cX, cY = int(M["m10"] / M["m00"]), int(M["m01"] / M["m00"])
            half_size = 276 // 2
            h, w = blurred.shape

            x1, y1 = max(0, cX - half_size), max(0, cY - half_size)
            x2, y2 = min(w, cX + half_size), min(h, cY + half_size)

            roi = blurred[y1:y2, x1:x2]
            if roi.shape[0] != 276 or roi.shape[1] != 276:
                roi = cv2.resize(roi, (276, 276))

    if roi is None:
        h, w = blurred.shape
        cX, cY = w // 2, h // 2
        roi = blurred[max(0, cY-138):min(h, cY+138), max(0, cX-138):min(w, cX+138)]
        roi = cv2.resize(roi, (276, 276))

    resized_roi = cv2.resize(roi, (138, 138))
    rgb_roi = cv2.cvtColor(resized_roi, cv2.COLOR_GRAY2RGB)
    return rgb_roi


